function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
	document.getElementById("mySidenav").style.transform="translateX(0px)";
}

function closeNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.getElementById("mySidenav").style.transform="translateX(-250px)";
}